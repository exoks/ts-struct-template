# ===[ Imports: ]==============================================================
from statsmodels.tsa.holtwinters import SimpleExpSmoothing, Holt
from statsmodels.tsa.holtwinters import ExponentialSmoothing


# ===[ build_simple_exp_smoothing: ]===========================================
def build_simple_exp_smoothing(train_set, params=None):
    return SimpleExpSmoothing(train_set)


# ===[ build_holt_linear_trend: ]==============================================
def build_holt_linear_trend(train_set, params=None):
    return Holt(train_set, damped_trend=True)


# ===[ build_holt_winters: ]===================================================
def build_holt_winters(train_set, params=None):
    return ExponentialSmoothing(train_set,
                                trend="add",
                                seasonal="add",
                                seasonal_periods=52)

# INFO:[ Holt winters models ]=================================================
# > (alpha, beta, gamma) are the parameter tuning where:
#   . alpha: controls the level
#   . beta: controls the trend
#   . gamma: controls the seasonality


