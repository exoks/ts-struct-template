# ===[ Imports: ]==============================================================
from utils.logger import getLogger
import pandas as pd
import config


# ===[ load_raw_data: ]========================================================
def load_raw_data() -> pd.DataFrame:
    logger = getLogger(__name__)
    logger.info(f"-> Loading ./{config.raw_data_path} ...")

    data = pd.read_csv(config.raw_data_path)
    # INFO:--------------------------------------------------------
    # - We can parse and set the index through loading process also
    #   pd.read_csv(config.raw_data_path,
    #                    index_col="Date",
    #                    parse_dates=True)
    return data


# ===[ save_processed_data: ]==================================================
def save_processed_data(data: pd.DataFrame):
    logger = getLogger(__name__)
    logger.info(f"-> Saving processed data ./{config.processed_data_path} ...")
    data.to_csv(config.processed_data_path)


# ===[ train_test_split ]======================================================
def train_test_split(data: pd.DataFrame, feature: str, period: int):
    logger = getLogger(__name__)
    logger.info("-> Train-Test Splitting the data ...")
    train_set = data.iloc[:-period, :]
    logger.debug(f"===============[train_set]===============\n{train_set}")
    test_set = data.iloc[-period:, :]
    logger.debug(f"================[test_set]===============\n{test_set}")
    return train_set[feature], test_set[feature]
